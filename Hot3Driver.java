
public class Hot3Driver {

	public Hot3Driver() {
		
	}
	
	public static int SickDriver(int num1, int num2) {
		int total;
		total = num1 + num2;
		return total;
	}
	
	public static int SickDriver(int num1, int num2, int num3) {
		int total;
		total = num1 + num2 + num3;
		return total;
	}

}
